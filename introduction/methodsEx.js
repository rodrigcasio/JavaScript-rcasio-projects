// Example of using methods
// method .toUpperCase() : converts every character of the string to uppercased
// .trim() : removes spaces from before the string and after string

console.log("Rodrigo Casio".toUpperCase()); // outputs RODRIGO CASIO
console.log("   I dont like white spaces    ".trim()); // outputs 'I dont like white spaces'

console.log("Tripiante".startsWith('T')); // outputs: true, since it does start the string with the character 'T'
console .log("Casionator".startsWith('T'));  // output: false, since it does not start the string with the character 'T'


