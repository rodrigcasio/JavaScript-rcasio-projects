// Learning built-in JavaScript method named .push()

// 1st Example using .push() method
const chores = ['wash dishes', 'do laundry', 'take out trash'];

chores.push('clean my room', 'clean the bath');
console.log(chores);    // making sure that they were added to the chores array


// 2nd example of using .push() method
const itemTracker = ['item 0', 'item 1', 'item 2'];

itemTracker.push('item 3', 'item 4');
console.log(itemTracker);








